    def __init__(self, keyWords, max_page=10):
        self.keyWords = keyWords
        self.page = 1
        self.max_page = max_page
        chrome_options = Options()
        # chrome_options.add_argument("--headless")
        prefs = {
            'profile.default_content_setting_values': {
                'images': 2,  # 不加载图片
                'javascript': 2,  # 不加载JS
            }
        }
        chrome_options.add_experimental_option("prefs", prefs)
        self.header = self.headers()
        chrome_options.add_argument(self.header)
        self.driver = webdriver.Chrome(options=chrome_options)
        self.driver.get('https://m.baidu.com')
        try:
            val = WebDriverWait(self.driver, 10).until(
                EC.presence_of_element_located((By.CSS_SELECTOR, '#word')))
            val.send_keys(self.keyWords)
            val.send_keys(Keys.ENTER)
            sleep(2)
            print('百度初始化完成')
        except:
            self.driver.quit()
            print('连接超时...')
            return

    def all_css(self, css_select):
        return self.driver.find_elements_by_css_selector(css_select)

    def __iter__(self):
        return self

    def __next__(self):
        items = self.all_css('.resitem')

        content_list = []
        for inx, item in enumerate(items):
            web_list = {
                'title': item.find_element_by_class_name('result_title').text,
                'url': item.find_element_by_class_name('site').text,
                'index': inx,
                'page': self.page + 1
            }
            content_list.append(web_list)
        if self.page < self.max_page:
            try:
                self.driver.find_element_by_class_name("nextOnly").click()
                print('下一页')
            except BaseException as e:
                try:
                    self.driver.find_element_by_class_name("next").click()
                except BaseException as e:
                    # 最后一页没找到
                    print("未找到翻页按钮，当前ua:%s" % self.header)
                    self.driver.quit()
            self.page += 1
            return content_list
        else:
            print('已抵达最大页数，停止遍历')
            self.driver.quit()
            raise StopIteration